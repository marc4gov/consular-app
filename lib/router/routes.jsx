FlowRouter.route('/', {
  name: "home",
  action: function(params, queryParams) {
    ReactLayout.render(App)
  }
});

FlowRouter.route('/private', {
  name: "private",
  triggersEnter: [AccountsTemplates.ensureSignedIn],
  action: function(params, queryParams) {
    ReactLayout.render(AppMain, {
      header: "Hoofd",
      content: <Welcome name="Prive" />,
      footer: "Staart"
    })
  }
});

FlowRouter.notFound = {
  action: function() {
    ReactLayout.render(AppMain, {
      header: "Hoofd",
      content: <Welcome name="Niet gevonden" />,
      footer: "Staart"
    })
  }
};


